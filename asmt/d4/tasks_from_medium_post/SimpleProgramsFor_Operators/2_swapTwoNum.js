/**
 * Swapping 2 numbers
 */

let a = 12.34;
let b = 46;

console.log(`Values BEFORE swapping are a: ${a}, b: ${b}`);

let tmp = a;
a = b;
b = tmp;

console.log(`Values AFTER swapping are a: ${a}, b: ${b}`);


// 03:00:20.815 Values BEFORE swapping are a: 12.34, b: 46 debugger eval code:9:9
